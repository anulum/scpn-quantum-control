# scpn-quantum-control — Unified Kuramoto toolkit facade
r"""Unified import surface for the Kuramoto toolkit.

Every classical-Kuramoto capability lives in :mod:`scpn_quantum_control.accel` as a focused,
single-responsibility module. This facade re-exports that whole surface under one import so a
caller writes ``from scpn_quantum_control.kuramoto import order_parameter, kuramoto_rk4_trajectory,
chimera_index`` rather than reaching into individual accelerator modules, and it adds a
**capability map** that organises the toolkit into coherent groups for discovery.

The groups are:

``forces``
    The instantaneous coupling forces ``F(θ)`` and their Jacobians — mean-field, networked,
    Sakaguchi, triadic, Daido, higher-order simplex / hyperedge, the heterogeneous mixer and the
    delayed forces.
``integrators``
    Time evolution of the phase flow — fixed-step Euler / RK4 and adaptive Dormand–Prince (each
    with a reverse-mode adjoint), plus the delayed, inertial, noisy and adaptive-coupling
    integrators and their trajectory containers.
``observables``
    The order parameter, mean phase, their network-local variants, the Daido generalised order
    parameter and the interaction energy, each with gradients / Hessians.
``diagnostics``
    Trajectory diagnostics — the frequency-locking indices, the chimera / metastability indices,
    the phase-distribution entropy and mutual information, the pairwise coherence matrices and the
    partial-synchronisation cluster metrics.
``analysis``
    Closed-form and spectral analysis — the synchronisation stability spectrum, the critical
    coupling, the Ott–Antonsen reduction, the Lyapunov spectrum, explosive-transition continuation,
    ring-state basins and the delayed / noisy mean-field theory.
``control_and_design``
    Terminal control objectives and their gradients, optimal-coupling and pinning design, and
    coupling system identification.
``types``
    The callable type aliases shared across the toolkit (forces, Jacobians, plasticity rules, …).
``dispatch``
    The multi-language dispatch core that selects the fastest available backend.
``tier_introspection``
    The ``last_*_tier_used`` helpers reporting which backend served the most recent call.

The facade is a thin re-export: every name resolves to exactly the object in
:mod:`scpn_quantum_control.accel`, so the two surfaces stay in lock-step (a parity guarded by the
test suite). Use :func:`capabilities` for the structured map and :func:`describe` for a readable
summary.
"""

