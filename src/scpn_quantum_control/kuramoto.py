#: The Kuramoto capability map: an immutable view of the coherent groups, each a tuple of the
#: public symbol names that group exposes. Its union is exactly the re-exported toolkit.
KURAMOTO_CAPABILITIES: Mapping[str, tuple[str, ...]] = MappingProxyType(_CAPABILITIES)


def capabilities() -> Mapping[str, tuple[str, ...]]:
    """Return the immutable Kuramoto capability map (group name → public symbol names)."""
    return KURAMOTO_CAPABILITIES


def describe(category: str | None = None) -> str:
    """Return a readable summary of the toolkit's capabilities.

    Parameters
    ----------
    category : str, optional
        A capability group name. When omitted, a one-line-per-group overview with symbol counts
        is returned; when given, the symbols of that group are listed one per line.

    Returns
    -------
    str
        The overview, or the named group's symbols.

    Raises
    ------
    ValueError
        If ``category`` is not one of the capability groups.
    """
    if category is None:
        return "\n".join(
            f"{group}: {len(symbols)} symbols" for group, symbols in KURAMOTO_CAPABILITIES.items()
        )
    if category not in KURAMOTO_CAPABILITIES:
        groups = ", ".join(KURAMOTO_CAPABILITIES)
        raise ValueError(f"unknown capability group {category!r}; choose one of: {groups}")
    return "\n".join(KURAMOTO_CAPABILITIES[category])


