# SCPN Quantum Control — Native dense-Hamiltonian speedup benchmark
"""Reproducible Rust-vs-Qiskit benchmark for dense XY-Hamiltonian construction.

This harness follows the unified GOTM benchmark standard
(`agentic-shared/BENCHMARK_STANDARD.md`) and mirrors the SCPN-CONTROL regression
apparatus: warm-up then repeats, P50/P95/P99 percentiles, one row per
(operation, backend), explicit unavailable backends, full provenance, and a
tamper-detection payload digest. It re-measures the dense XY-Hamiltonian build
that the obsolete "5401x faster than Qiskit" headline referred to; the backends
are the Rust PyO3 kernel and the Qiskit ``SparsePauliOp`` dense path, fed
identical ``K``/``omega`` so the ratio is apples-to-apples and parity-checked.

The numbers are environment-dependent (they swing with CPU pinning, BLAS
threading, and host load), so the artefact is marked ``production_claim_allowed:
false`` — it is a reproducible local regression guard, not a published
performance claim. The canonical figure is regenerated by the CI workflow and
compared against the committed baseline by ``tools/benchmark_native_speedup_gate.py``.
"""

from __future__ import annotations

import argparse
import hashlib
import importlib.util
import json
import os
import platform
import subprocess
import sys
import time
from collections.abc import Callable
from datetime import datetime, timezone
from functools import partial
from pathlib import Path
from typing import Any

import numpy as np
from numpy.typing import NDArray

if sys.version_info >= (3, 11):
    import tomllib
else:  # pragma: no cover - exercised on Python 3.10
    import tomli as tomllib

from scpn_quantum_control.accel.rust_import import optional_rust_engine
from scpn_quantum_control.bridge import build_knm_paper27
from scpn_quantum_control.bridge.knm_hamiltonian import knm_to_hamiltonian

_HAS_RESOURCE = importlib.util.find_spec("resource") is not None

REPORT_SCHEMA = "scpn-quantum-control.native-speedup.v1"
BASELINE_SCHEMA = "scpn-quantum-control.native-speedup-baseline.v1"
REPO_ROOT = Path(__file__).resolve().parents[1]
RUST_CARGO = REPO_ROOT / "scpn_quantum_engine" / "Cargo.toml"
DEFAULT_OUT_DIR = REPO_ROOT / "data" / "native_speedup"
SYSTEM_SIZES = (4, 8, 10, 12)


def _stats(samples_us: list[float]) -> dict[str, float]:
    """Return P50/P95/P99 and throughput from microsecond samples."""
    ordered = sorted(samples_us)
    count = len(ordered)

    def _pct(fraction: float) -> float:
        return ordered[min(count - 1, int(fraction * (count - 1)))]

    mean_us = float(np.mean(ordered))
    return {
        "p50_us": float(_pct(0.50)),
        "p95_us": float(_pct(0.95)),
        "p99_us": float(_pct(0.99)),
        "throughput_ops_s": float(1.0e6 / mean_us) if mean_us > 0 else 0.0,
    }


def _measure(fn: Callable[[], object], *, warmup: int, repeats: int) -> dict[str, float]:
    """Warm up (discarded), then collect ``repeats`` perf-counter samples."""
    for _ in range(warmup):
        fn()
    samples_us: list[float] = []
    for _ in range(repeats):
        start = time.perf_counter_ns()
        fn()
        samples_us.append((time.perf_counter_ns() - start) / 1_000.0)
    return _stats(samples_us)


def _problem(system_size: int) -> tuple[NDArray[np.float64], NDArray[np.float64]]:
    """Return a symmetric coupling matrix and frequency vector for ``L`` oscillators."""
    coupling = build_knm_paper27(L=system_size)
    coupling = (coupling + coupling.T) / 2.0
    frequencies = np.linspace(0.1, 0.4, system_size, dtype=np.float64)
    return coupling, frequencies


def _rust_dense(
    engine: Any, coupling: NDArray[np.float64], frequencies: NDArray[np.float64], size: int
) -> NDArray[np.float64]:
    """Build the dense XY Hamiltonian operator through the Rust PyO3 kernel.

    The XY Hamiltonian is real-symmetric, so the kernel returns ``float64``; this
    is the natural operator dtype. The production ``knm_to_dense_matrix`` wrapper
    additionally casts to ``complex128`` for a complex-typed downstream API — that
    cast is a downstream cost, not part of the construction kernel, and is
    excluded from this comparison.
    """
    flat = np.asarray(
        engine.build_xy_hamiltonian_dense(
            coupling.ravel().astype(np.float64), frequencies.astype(np.float64), size
        )
    )
    return flat.reshape(2**size, 2**size)


def _qiskit_dense(
    coupling: NDArray[np.float64], frequencies: NDArray[np.float64]
) -> NDArray[np.complex128]:
    """Build the dense XY Hamiltonian through the Qiskit ``SparsePauliOp`` path."""
    return np.asarray(knm_to_hamiltonian(coupling, frequencies).to_matrix(), dtype=np.complex128)


def _repeats_for(size: int) -> tuple[int, int]:
    """Return ``(warmup, repeats)`` scaled to the dense build cost at ``L``."""
    if size <= 6:
        return 5, 200
    if size <= 8:
        return 3, 80
    if size <= 10:
        return 2, 25
    return 2, 8


def benchmark_dense_hamiltonian(sizes: tuple[int, ...]) -> tuple[dict[str, Any], bool]:
    """Benchmark the dense XY-Hamiltonian build; return (benchmarks, rust_seen)."""
    engine = optional_rust_engine()
    rust_available = engine is not None and hasattr(engine, "build_xy_hamiltonian_dense")
    benchmarks: dict[str, Any] = {}

    for size in sizes:
        warmup, repeats = _repeats_for(size)
        coupling, frequencies = _problem(size)
        languages: dict[str, Any] = {}

        qiskit_stats = _measure(
            partial(_qiskit_dense, coupling, frequencies), warmup=warmup, repeats=repeats
        )
        languages["qiskit_sparsepauliop"] = qiskit_stats

        parity: bool | None = None
        speedup: float | None = None
        if rust_available:
            rust_stats = _measure(
                partial(_rust_dense, engine, coupling, frequencies, size),
                warmup=warmup,
                repeats=repeats,
            )
            languages["rust_pyo3"] = rust_stats
            parity = bool(
                np.allclose(
                    _rust_dense(engine, coupling, frequencies, size).astype(np.complex128),
                    _qiskit_dense(coupling, frequencies),
                    atol=1e-9,
                )
            )
            speedup = (
                qiskit_stats["p50_us"] / rust_stats["p50_us"] if rust_stats["p50_us"] > 0 else None
            )

        benchmarks[f"dense_xy_hamiltonian_L{size}"] = {
            "languages": languages,
            "hilbert_dim": 2**size,
            "warmup": warmup,
            "repeats": repeats,
            "cross_language_parity": parity,
            "speedup_p50_rust_vs_qiskit": speedup,
            "rust_available": rust_available,
        }
    return benchmarks, rust_available


def _cpu_model() -> str:
    """Return the CPU model string from ``/proc/cpuinfo`` when available."""
    cpuinfo = Path("/proc/cpuinfo")
    if cpuinfo.exists():
        for line in cpuinfo.read_text(encoding="utf-8").splitlines():
            if line.startswith("model name"):
                return line.split(":", 1)[1].strip()
    return platform.processor() or "unknown"


def _affinity() -> list[int] | None:
    """Return the CPU affinity set, or ``None`` where it is unavailable."""
    try:
        return sorted(os.sched_getaffinity(0))
    except AttributeError:  # pragma: no cover - not on Linux.
        return None


def _loadavg() -> list[float] | None:
    """Return the 1/5/15-minute load average, or ``None`` where unavailable."""
    try:
        return [round(value, 2) for value in os.getloadavg()]
    except (OSError, AttributeError):  # pragma: no cover - not on Linux.
        return None


def _git_commit() -> str:
    """Return the HEAD commit, or ``unknown`` outside a checkout."""
    try:
        result = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            cwd=REPO_ROOT,
            capture_output=True,
            text=True,
            check=False,
        )
        return result.stdout.strip() or "unknown"
    except OSError:
        return "unknown"


def _rust_release_profile() -> dict[str, Any]:
    """Read the crate ``[profile.release]`` so flags are recorded, not guessed."""
    try:
        with RUST_CARGO.open("rb") as handle:
            data = tomllib.load(handle)
        profile: dict[str, Any] = data.get("profile", {}).get("release", {})
        return profile
    except (OSError, tomllib.TOMLDecodeError):
        return {}


def _peak_rss_mb() -> float:
    """Return peak resident set size in MiB, or ``0.0`` where unavailable."""
    if not _HAS_RESOURCE:  # pragma: no cover - Windows has no resource module.
        return 0.0
    import resource

    return round(resource.getrusage(resource.RUSAGE_SELF).ru_maxrss / 1024.0, 1)


def _engine_label() -> str:
    """Return an identifier for the installed Rust engine, or its absence."""
    engine = optional_rust_engine()
    if engine is None:
        return "absent"
    return str(getattr(engine, "__version__", "installed"))


def payload_digest(payload: dict[str, Any]) -> str:
    """Return the SHA-256 of a payload, excluding any existing digest field."""
    body = {key: value for key, value in payload.items() if key != "payload_sha256"}
    serialised = json.dumps(body, sort_keys=True, separators=(",", ":")).encode()
    return hashlib.sha256(serialised).hexdigest()


def canonical_metrics_digest(benchmarks: dict[str, Any]) -> str:
    """Return the SHA-256 of the benchmark metrics for baseline tamper detection."""
    serialised = json.dumps(benchmarks, sort_keys=True, separators=(",", ":")).encode()
    return hashlib.sha256(serialised).hexdigest()


def build_report(
    sizes: tuple[int, ...], *, evidence_class: str, generated_utc: str
) -> dict[str, Any]:
    """Assemble the full benchmark report with provenance and a payload digest."""
    load_start = _loadavg()
    benchmarks, rust_seen = benchmark_dense_hamiltonian(sizes)
    report = {
        "schema_version": REPORT_SCHEMA,
        "generated_utc": generated_utc,
        "evidence_class": evidence_class,
        "production_claim_allowed": False,
        "provenance": {
            "cpu_model": _cpu_model(),
            "cpu_count": os.cpu_count(),
            "rust_release_profile": _rust_release_profile(),
            "rust_backend": "present" if rust_seen else "absent",
            "engine": _engine_label(),
            "python": platform.python_version(),
            "platform": platform.platform(),
            "commit": _git_commit(),
            "cpu_affinity": _affinity(),
            "loadavg_start": load_start,
            "loadavg_end": _loadavg(),
            "peak_rss_mb": _peak_rss_mb(),
        },
        "note": (
            "Dense XY-Hamiltonian construction kernel: rust_pyo3 (real float64) vs "
            "qiskit_sparsepauliop, parity-checked. The production knm_to_dense_matrix "
            "wrapper adds a float64->complex128 cast that dominates at large L "
            "(excluded here). Environment-dependent; not a published claim."
        ),
        "benchmarks": benchmarks,
    }
    report["payload_sha256"] = payload_digest(report)
    return report


def report_to_baseline(report: dict[str, Any]) -> dict[str, Any]:
    """Convert a fresh report into an admitted baseline with a tamper digest."""
    benchmarks = report["benchmarks"]
    baseline = {
        "schema_version": BASELINE_SCHEMA,
        "baseline_commit": report["provenance"]["commit"],
        "measured_utc": report["generated_utc"],
        "evidence_class": report["evidence_class"],
        "production_claim_allowed": False,
        "provenance": report["provenance"],
        "benchmarks": benchmarks,
    }
    baseline["baseline_sha256"] = canonical_metrics_digest(benchmarks)
    return baseline


def main(argv: list[str] | None = None) -> int:
    """Run the benchmark and write the report and/or baseline artefacts."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--sizes", type=int, nargs="+", default=list(SYSTEM_SIZES))
    parser.add_argument("--pin-core", type=int, default=None)
    parser.add_argument("--evidence-class", default="local_regression")
    parser.add_argument("--json-out", type=Path, default=None)
    parser.add_argument("--write-baseline", type=Path, default=None)
    args = parser.parse_args(argv)

    if args.pin_core is not None:
        os.sched_setaffinity(0, {args.pin_core})

    report = build_report(
        tuple(args.sizes),
        evidence_class=args.evidence_class,
        generated_utc=datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
    )

    if args.json_out is not None:
        out_path = args.json_out
    else:
        out_path = DEFAULT_OUT_DIR / f"native_speedup_{report['generated_utc'][:10]}.local.json"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(report, indent=2, sort_keys=True) + "\n", encoding="utf-8")

    if args.write_baseline is not None:
        baseline = report_to_baseline(report)
        args.write_baseline.parent.mkdir(parents=True, exist_ok=True)
        args.write_baseline.write_text(
            json.dumps(baseline, indent=2, sort_keys=True) + "\n", encoding="utf-8"
        )

    print(f"schema={report['schema_version']} cpu={report['provenance']['cpu_model']}")
    for name, bench in report["benchmarks"].items():
        speedup = bench.get("speedup_p50_rust_vs_qiskit")
        speedup_text = f"{speedup:.2f}x" if speedup is not None else "n/a"
        print(
            f"  {name}: speedup_p50_vs_qiskit={speedup_text} parity={bench['cross_language_parity']}"
        )
    print(f"report -> {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
