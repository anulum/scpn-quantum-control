def test_cli_write_preview_check_and_failure_round_trip(
    tmp_path: Path,
    capsys: pytest.CaptureFixture[str],
) -> None:
    """The CLI writes, previews, validates, detects drift, and fails bad inputs."""
    output = tmp_path / "reviewer.md"
    args = [
        "--output",
        str(output),
        "--manifest",
        str(DEFAULT_CAPABILITY_MANIFEST_PATH),
        "--support-matrix",
        str(DEFAULT_SUPPORT_MATRIX_PATH),
        "--changelog",
        str(DEFAULT_CHANGELOG_PATH),
        "--roadmap",
        str(DEFAULT_ROADMAP_PATH),
    ]
    assert main(["--write", *args]) == 0
    assert output.is_file()
    assert main(["--check", *args]) == 0
    assert "current" in capsys.readouterr().out
    output.write_text("stale\n", encoding="utf-8")
    assert main(["--check", *args]) == 1
    assert "is stale" in capsys.readouterr().err
    assert main(["--manifest", str(DEFAULT_CAPABILITY_MANIFEST_PATH)]) == 0
    assert "# Differentiable Reviewer Evidence" in capsys.readouterr().out
    bad_manifest = tmp_path / "bad.json"
    bad_manifest.write_text("{}", encoding="utf-8")
    assert main(["--write", "--manifest", str(bad_manifest), *args[:2]]) == 1
    assert "FAIL" in capsys.readouterr().err


