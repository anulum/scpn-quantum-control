def _payload() -> dict[str, object]:
    """Return a mutable fresh payload without reading the support page."""
    return build_differentiable_reviewer_evidence_payload(support_summary=SUMMARY)


def _rows(payload: dict[str, object]) -> list[dict[str, object]]:
    """Return the mutable evidence-row list from a test payload."""
    value = payload["rows"]
    assert isinstance(value, list)
    assert all(isinstance(row, dict) for row in value)
    return cast(list[dict[str, object]], value)


def _gaps(payload: dict[str, object]) -> list[dict[str, object]]:
    """Return the mutable open-gap list from a test payload."""
    value = payload["open_gaps"]
    assert isinstance(value, list)
    assert all(isinstance(row, dict) for row in value)
    return cast(list[dict[str, object]], value)


def _write_support_page(path: Path, *, registry: str = "118/118", planner: str = "5/5") -> None:
    """Write the minimum valid generated support-page summary fixture."""
    supported, blocked = planner.split("/")
    path.write_text(
        "<!-- Generated by tools/differentiable_support_matrix_page.py; do not edit by hand. -->\n"
        "[Differentiable Reviewer Evidence](differentiable_reviewer_evidence.md)\n"
        f"| Program AD registry | `{registry}` complete | evidence |\n"
        f"| Quantum-gradient planner audit | `{supported}` supported / `{blocked}` fail-closed | evidence |\n",
        encoding="utf-8",
    )


